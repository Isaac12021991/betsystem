   function retirar()
   {

                  var ca_monto = $('#ca_monto').val();


             
         if (ca_monto == '') {
   
          toastr.error("Ingrese el monto", 'Error');
           $('#ca_monto').focus();
            return;
   
       };

   
            ca_monto = ca_monto.replace(",",".");


            var data = new FormData();

            data.append('ca_monto', ca_monto);

            var url = "<?php echo site_url('wallet/ejecutar_retirar') ?>";

            KTApp.blockPage({
              overlayColor: 'black',
              opacity: 0.2,
              state: 'primary' // a bootstrap color
             });


                $.ajax({
                url:url,
                type:'POST',
                contentType:false,
                data:data,
                processData:false,
                cache:false}).done(function( data ) { 

                    KTApp.unblockPage();
                   var obj = JSON.parse(data);



                       if (obj.error == 0) {

                     $(location).attr('href',"<?php echo site_url() ?>wallet/index");
   

                       }else{
              
                          toastr.error(obj.message, 'Error');
                         return;

                       }
                                   

                      }); 


   
   
   }
   

   
                                      
